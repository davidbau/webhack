/**
 * Big Room variant 4 (diamond/rhombus shape)
 * Simplified port from nethack-c/dat/bigrm-5.lua
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel', 'noflip');

    // Diamond-shaped big room
    des.map({
        map: `
                            ------------------
                    ---------................---------
              -------................................-------
         ------............................................------
      ----......................................................----
    ---............................................................---
  ---................................................................---
---....................................................................---
|........................................................................|
|........................................................................|
|........................................................................|
---....................................................................---
  ---................................................................---
    ---............................................................---
      ----......................................................----
         ------............................................------
              -------................................-------
                    ---------................---------
                            ------------------
`
    });

    // Light the room
    des.region(selection.area(0, 0, 72, 18), 'lit');

    // Stairs
    des.stair('up');
    des.stair('down');

    // Non-diggable walls
    des.non_diggable();

    // Objects
    for (let i = 0; i < 15; i++) {
        des.object();
    }

    // Traps
    for (let i = 0; i < 6; i++) {
        des.trap();
    }

    // Monsters
    for (let i = 0; i < 28; i++) {
        des.monster();
    }

    return finalize_level();
}
