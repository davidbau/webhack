/**
 * Library Level (halls of knowledge)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel');

    // Library with bookcases (represented as walls creating alcoves)
    des.map({
        map: `
---------------------------------------------------------------------------
|.........................................................................|
|..|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|
|.........................................................................|
|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|...|
|.........................................................................|
|..|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|
|.........................................................................|
|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|...|
|.........................................................................|
|..|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|
|.........................................................................|
|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|...|
|.........................................................................|
|..|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|
|.........................................................................|
|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|.|...|
|.........................................................................|
|.........................................................................|
---------------------------------------------------------------------------
`
    });

    // Well-lit library
    des.region(selection.area(1, 1, 73, 17), 'lit');

    // Stairs
    des.stair('up');
    des.stair('down');

    // Non-diggable walls
    des.non_diggable();

    // Many scrolls and books
    for (let i = 0; i < 20; i++) {
        des.object({ class: '?' }); // Scrolls
    }
    for (let i = 0; i < 10; i++) {
        des.object({ id: 'spellbook' });
    }

    // Other objects
    for (let i = 0; i < 10; i++) {
        des.object();
    }

    // Traps
    for (let i = 0; i < 6; i++) {
        des.trap();
    }

    // Scholars and wizards
    des.monster({ id: 'wizard' });
    des.monster({ id: 'wizard' });
    des.monster({ id: 'wizard' });
    des.monster({ id: 'Elvenking' });
    des.monster({ id: 'elven wizard' });
    des.monster({ id: 'elven wizard' });
    des.monster({ id: 'master mind flayer' });
    des.monster({ id: 'master lich' });
    des.monster({ id: 'arch-lich' });

    // Random monsters
    for (let i = 0; i < 19; i++) {
        des.monster();
    }

    return finalize_level();
}
