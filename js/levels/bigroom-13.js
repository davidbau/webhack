/**
 * Bigroom variant 13: Pillars with various patterns
 * C ref: nethack-c/dat/bigrm-13.lua
 */

import * as des from '../sp_lev.js';
import { selection } from '../sp_lev.js';
import { rn2 } from '../rng.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });
    des.level_flags('mazelevel', 'noflip');

    des.map(`
---------------------------------------------------------------------------
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
|.........................................................................|
---------------------------------------------------------------------------
`);

    const pillar = `
---
| |
---`;

    const filters = [
        // 1: all pillars
        (x, y) => true,
        // 2: 3 vertical lines
        (x, y) => (x % 2 === 1),
        // 3: checkerboard
        (x, y) => (((x + y) % 2) === 0),
        // 4: center row
        (x, y) => (y % 2 === 1),
        // 5: top and bottom rows
        (x, y) => (y % 2 === 0),
        // 6: random 50%
        (x, y) => (rn2(2) === 0),
        // 7: corners and center
        (x, y) => (Math.floor(x / 3) % 2 === y % 2),
        // 8: slanted
        (x, y) => (Math.floor((x + 1) / 3) === y)
    ];

    const idx = rn2(filters.length);

    for (let y = 0; y <= 2; y++) {
        for (let x = 0; x <= 6; x++) {
            if (filters[idx](x, y)) {
                des.map({ coord: [12 + x * 9, 4 + y * 5], map: pillar, contents: () => {} });
            }
        }
    }

    des.region(selection.area(0, 0, 75, 18), 'lit');
    des.wallify();
    des.non_diggable();

    des.stair('up');
    des.stair('down');

    for (let i = 0; i < 15; i++) {
        des.object();
    }
    for (let i = 0; i < 6; i++) {
        des.trap();
    }
    for (let i = 0; i < 28; i++) {
        des.monster();
    }

    return des.finalize_level();
}
