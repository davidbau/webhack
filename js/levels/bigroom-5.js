/**
 * Big Room variant 5 (four-lobed clover shape)
 * Simplified port from nethack-c/dat/bigrm-6.lua
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel', 'noflip');

    // Four-lobed clover-shaped room with trees and fountains
    des.map({
        map: `
     ---------         ---------         ---------         ---------
   ---.......---     ---.......---     ---.......---     ---.......---
  --...........--   --...........--   --...........--   --...........--
 --.............-- --.............-- --.............-- --.............--
 -...............- -...............- -...............- -..............-
--...............---...............---...............---...............--
|.................-.................-.................-.................|
|........T.................T.................T.................T........|
|.......................................................................|
|......T.{.....................................................{.T......|
|.......................................................................|
|........T.................T.................T.................T........|
|.................-.................-.................-.................|
--...............---...............---...............---...............--
 -...............- -...............- -...............- -..............-
 --.............-- --.............-- --.............-- --.............--
  --...........--   --...........--   --...........--   --...........--
   ---.......---     ---.......---     ---.......---     ---.......---
     ---------         ---------         ---------         ---------
`
    });

    // Light the room
    des.region(selection.area(1, 1, 72, 17), 'lit');

    // Stairs
    des.stair('up');
    des.stair('down');

    // Non-diggable walls
    des.non_diggable();

    // Objects
    for (let i = 0; i < 15; i++) {
        des.object();
    }

    // Traps
    for (let i = 0; i < 6; i++) {
        des.trap();
    }

    // Monsters
    for (let i = 0; i < 28; i++) {
        des.monster();
    }

    return finalize_level();
}
