/**
 * Arena Level (gladiatorial combat)
 * Original design for NetHack JavaScript port
 */

import { des, selection, finalize_level } from '../sp_lev.js';

export function generate() {
    des.level_init({ style: 'solidfill', fg: ' ' });

    des.level_flags('mazelevel');

    // Circular arena with viewing stands
    des.map({
        map: `
---------------------------------------------------------------------------
|.........................................................................|
|.....................-----------.........-----------....................|
|...................--|..........|-------|..........|--..................|
|...................|..............................................|......|
|..................|................................................|.....|
|.................|..................................................|....|
|................|....................................................|...|
|................|.....................................................|..|
|................|......................................................|.|
|................|......................................................|.|
|................|.....................................................|..|
|................|....................................................|...|
|.................|..................................................|....|
|..................|................................................|.....|
|...................|..............................................|......|
|...................--|..........|-------|..........|--..................|
|.....................-----------.........-----------....................|
|.........................................................................|
---------------------------------------------------------------------------
`
    });

    // Light the arena
    des.region(selection.area(1, 1, 73, 18), 'lit');

    // Stairs outside arena
    des.stair('up', 5, 5);
    des.stair('down', 68, 15);

    // Non-diggable walls
    des.non_diggable();

    // Objects
    for (let i = 0; i < 15; i++) {
        des.object();
    }

    // Traps
    for (let i = 0; i < 8; i++) {
        des.trap();
    }

    // Gladiators and warriors
    des.monster({ id: 'soldier' });
    des.monster({ id: 'soldier' });
    des.monster({ id: 'soldier' });
    des.monster({ id: 'soldier' });
    des.monster({ id: 'sergeant' });
    des.monster({ id: 'sergeant' });
    des.monster({ id: 'lieutenant' });
    des.monster({ id: 'captain' });
    des.monster({ id: 'valkyrie' });
    des.monster({ id: 'barbarian' });
    des.monster({ id: 'samurai' });

    // Arena beasts
    des.monster({ id: 'tiger' });
    des.monster({ id: 'tiger' });
    des.monster({ id: 'lion' });
    des.monster({ id: 'panther' });
    des.monster({ id: 'owlbear' });

    // Random monsters
    for (let i = 0; i < 12; i++) {
        des.monster();
    }

    return finalize_level();
}
